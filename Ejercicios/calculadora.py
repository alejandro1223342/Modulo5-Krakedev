alto = float(input("Ingresa el Alto (A): "))
ancho = float(input("Ingresa el Ancho (H): "))

area_rectangulo = alto * ancho

perimetro_rectangulo = 2 * (alto+ancho)

seperficie_rectangulo = area_rectangulo

print(f"Area: {area_rectangulo}")
print(f"Perimetro: {perimetro_rectangulo}")
print(f"Superficie: {seperficie_rectangulo}")

